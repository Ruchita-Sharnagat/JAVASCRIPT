let fruit = "Mango";

switch (fruit) {
    case "Apple":
        console.log("Apples are red.");
        break;
    case "Banana":
        console.log("Bananas are yellow.");
        break;
    case "Mango":
        console.log("Mangoes are sweet.");
        break;
    default:
        console.log("Unknown fruit");
}
